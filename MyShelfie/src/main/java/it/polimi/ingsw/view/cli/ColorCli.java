package it.polimi.ingsw.view.cli;

/**
 * enum used for coloring the cli
 */
public class ColorCli {
    //Reset
    public static final String RESET = "\u001B[0m";

    public static final String PINK = "\u001B[95m";
    public static final String RED = "\u001B[31m";
    public static final String BLUE = "\u001B[34m";
    public static final String GREEN = "\u001B[32m";
    public static final String WHITE = "\u001B[37m";
    public static final String CYAN = "\u001B[36m";
    public static final String YELLOW = "\u001B[33m";
    public static final String BLACK = "\u001b[30m";
    public static final String BACK_CYAN = "\u001b[46;1m";
    public static final String BACK_BLUE = "\u001b[44;1m";
    public static final String BACK_GREEN = "\u001b[42;1m";
    public static final String BACK_YELLOW = "\u001b[43;1m";
    public static final String BACK_PURPLE = "\u001b[45;1m";
    public static final String BACK_WHITE = "\u001b[47;1m";
    public static final String BACK_BLACK = "\u001b[40;1m";

    public static final String YELLOW_BOLD = "\033[1;33m"; // YELLOW
    public static final String CYAN_BOLD ="\033[1;36m";    // CYAN
}
